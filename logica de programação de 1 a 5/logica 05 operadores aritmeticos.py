# Operadores Aritméticos

"""
Operadores
+ = adição
- = subtração
* = multiplicação
/ = divisão
// = divisão inteira
** = exponenciação
% = resto da divisão
() = alteração de precedencia
OBS: o operador * tambem pode ser usado para a multiplicação de strings
exp: print(20*'A')
"""

print(20 + 25)  # soma simples de adição
print(20 * 5)  # soma simples de multiplicação
print(20 / 2)  # soma simples de divisão
print(10.5 // 3)  # soma de divisão inteira com ponto flutuante (float)
print(5 ** 2)  # soma de exponenciação que no caso seria valor elevado a outro valor
print(10 % 3)  # resto da divisão, a soma é feita a partir de numeros em que a divisão não possui resultados inteiros
print(5+2*10)  # soma entre numeros inteiros sem a separação de ()
print((5+2)*10)  # agora vejamos um caso em que separamos 5+2 entre () para obtermos valores diferentes

